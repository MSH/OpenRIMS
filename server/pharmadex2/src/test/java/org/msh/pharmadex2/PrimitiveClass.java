package org.msh.pharmadex2;
/**
 * For test purpose, contains only primitives
 * @author alexk
 *
 */
public class PrimitiveClass {
	byte	byteProp=1;
	short shortProp=1;
	int intProp=1;
	long longProp=1l;
	float floatProp=1.1f;
	double doubleProp=1.1d;
	boolean booleanProp=true;
	char charProp='a';
	public byte getByteProp() {
		return byteProp;
	}
	public void setByteProp(byte byteProp) {
		this.byteProp = byteProp;
	}
	public short getShortProp() {
		return shortProp;
	}
	public void setShortProp(short shortProp) {
		this.shortProp = shortProp;
	}
	public int getIntProp() {
		return intProp;
	}
	public void setIntProp(int intProp) {
		this.intProp = intProp;
	}
	public long getLongProp() {
		return longProp;
	}
	public void setLongProp(long longProp) {
		this.longProp = longProp;
	}
	public float getFloatProp() {
		return floatProp;
	}
	public void setFloatProp(float floatProp) {
		this.floatProp = floatProp;
	}
	public double getDoubleProp() {
		return doubleProp;
	}
	public void setDoubleProp(double doubleProp) {
		this.doubleProp = doubleProp;
	}
	public boolean isBooleanProp() {
		return booleanProp;
	}
	public void setBooleanProp(boolean booleanProp) {
		this.booleanProp = booleanProp;
	}
	public char getCharProp() {
		return charProp;
	}
	public void setCharProp(char charProp) {
		this.charProp = charProp;
	}
	

}
